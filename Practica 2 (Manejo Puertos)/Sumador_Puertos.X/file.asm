    ;CONFIG6H DIR 30000BH	E0
    CONFIG	 WRTC=OFF	 ;CONFIGURACION DE REGISTROS NO PROTEGIDO
    CONFIG	 WRTB=OFF	 ;BLOQUE BOOTEBLE NO PROTEGIDO
    CONFIG	 WRTD=OFF	 ;EEPROMDE DATOS NO PROTGIDA

    ;CONFIG7L DIR 30000CH	0F
    CONFIG	 EBTR0=OFF	 ;TABLAS DE LETURA NO PROTEGIDAS
    CONFIG	 EBTR1=OFF
    CONFIG	 EBTR2=OFF
    CONFIG	 EBTR3=OFF

    ;CONFIG7H DIR 30000DH	40
    CONFIG	 EBTRB=OFF	 ;TABLAS NO PROTEGIDAS


NUM1    EQU  .80 ;Asigna el valor 80 a DAA
NUM2    EQU  .81 ;Asigna el valor 81 a DAA

    ORG  .0

INICIO
    CLRF     PORTA
    CLRF     PORTB
    CLRF     TRISB   ; TB = 0000 0000 (8 Salidas)
    SETF     TRISA   ; TA = 0111 1111 (7 Entradas)
    MOVLW    .15     ; Asigna 15 a W
    MOVWF    ADCON1  ; Asigna 1111 (15 Binario) a ADCON1 para indicar que
		     ; Todas las entradas son digitales

    MOVLW    .7      ; Asigna 7 a W
    MOVWF    CMCON   ; Asigna 111 (7 Binario) a ADCON1 para indicar que
		     ; Esta apagado el comparador de voltaje

SUMA
    MOVF    PORTA, W, 0 ; Asigna el valor de PORTA a W
			; W = 01110111

    ANDLW   B'00000111' ; Aplicar una mascara para obtener el valor de
			; RA0 RA1 RA2

    MOVWF   NUM1, 1     ; Se mueve el valor de W A NUM1


    MOVF    PORTA, W, 0 ; Asigna el valor de PORTA a W
			; W = 01110111

    RRNCF   WREG, F, 0	; W = 10111011
    RRNCF   WREG, F, 0	; W = 11011101
    RRNCF   WREG, F, 0	; W = 11101110
    RRNCF   WREG, F, 0	; W = 01110111

    ANDLW   B'00000111' ; Aplicar una mascara para obtener el valor de
			; RA6 RA5 RA4

    MOVWF   NUM2, 1     ; Se mueve el valor de W A NUM2

    ADDWF   NUM1, W, 1  ; Se suma el valor de W y NUM1 y se almacena en W

    MOVWF   PORTB, 0    ; Mueve el valor de W a PORTB
    GOTO    SUMA

    END
