; Este prgrama es un sumador en paralelo de 3 bits
; Suma cantidades de 3 bits por lo que el mayor resultado es 14
; La cantidad 1 se encuentra en RA2 RA1 RA0
; La cantidad 2 se encuentra en RA6 RA5 RA4
; El resultado se muestra en PORTB (4 bits de salida)

    LIST P=18F4550

    #INCLUDE <P18F4550.INC>
    
    ;CONFIG1L dir 300000h 	20
    ;CONFIG	PLLDIV=1	 ; 
